a = int(input("Enter your 1st number:"))
b = int(input("Enter your 2nd number:"))
c = (input("Enter your operand{+ - * /}:"))
if c == "+":
    print("The sum is:",a+b)
if c == "-":
    print("The difference is:",a-b)
if c == "*":
    print("The product is:",a*b)
if c == "/":
    print("The quotient is:",a/b)